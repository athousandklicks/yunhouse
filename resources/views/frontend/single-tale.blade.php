                		<h1>Yunhouse in Brief</h1>
                		
                		<p class="lead">In the year 1884, in the German City of Berlin, representatives of major European nations gathered around a giant map of the continent of Africa for the biggest imperialistic banquet in the history of mankind. Slices of the Dark Continent were served out to satisfy imperial hunger for territories – The Scramble For Africa – that had reached fever pitch.</p>

                		<div id="post-content">
                           <p>There is no record of any Africans’ presence at the Berlin Conference. Nor any evidence that Africans were ever informed or knew about the meeting. In fact, Africans got to know about the carve-up of their continent when Europeans turned up on their homelands with all manner of ploys to assert and claim the territorial possessions as apportioned in Berlin. The process was called The Pacification Of The Natives.</p>

                           <p>
                            <img src="images/slides/main-slider1.png" class="img-responsive" alt="">
                        </p>

                        <p>Yunhouse is founded by Africans whose personal experience of The Pacification Of The Natives is the Europeanisation of their minds through European education. Foundation inhabitants of Yunhouse identify themselves as a generation of Africans born within blood-dripping distance of The Great Berlin Carve-up. They are all students in London. They feel united by the uniqueness of being in London where they have insider perspective of the machinations of The Pacification Of The Natives: the conception, the design and the execution of colonial subjugation of their continent and its peoples. They form themselves into The Berliner Cult. Their motto: We shall never be pacified!</p>

                        <p>Their initial meetings in a basement flat in Camden Town, north London, are characteristically noisy as the Berliners revel in crying their eyes out or laughing their bellies off over issues pertaining to their beloved continent. Lekwot Abaka, founder member in whose flat the meetings takes place, is given quit notice by his English landlady who could no longer stand the volume of the noise – especially the guffawing – from her basement.</p>

                        <p>Luckily for The Berliners, a recent member to the Berliner Cult is Big Mnama, a young African who has been adopted from an African colony by Lord Kemp, a poet laureate and an English aristocrat of the fullblooded variety. Lord Kemp utters ‘fuck!’ when he hears the news of what happened to the African tenant in Camden Town, he then wills his estate in Hampstead to Big Mnama and fellow Berliners as a place where everyone would be free to laugh and cry about Africa, regardless of race or colour.</p>

                        <p>In The First Battle for Yunhouse, Big Mnama’s niece (full-blooded English girl) challenges her grandfather’s will giving Africa House to the Berliners. Race riots break out when a lower court says the Africans do not – and cannot – own Africa House. An appeal court later rules otherwise.</p>

                        <p>The story of Yunhouse unfolds as a narration of the evolution of an establishment dedicated to managing “intellectual emissions on Africa, be it ideation, invocation, evocation, incitation, excitation, expiation, agitation, agonisation, lamentation, jubilation, or – you name it, as long as the subject is Our Africa”. It is a weave where the threads are the characters, who – themselves – are really the tales in The Assorted London Tales About The Africa. They are tales because they are too true to be anything else. Scene by scene, the narration zooms in and out on the characters as they live, express, and display their synergy with the charged environment of the novel.</p>

                        <p>Principal characters include: Berliner Cult members (especially the two co-founders Lekwot Abaka and Cyril Naikule, and Ogesayi Mawe the only founder member to become president of his country); The Africa Picture [No Spitting!] news-magazine and it’s Editor for nearly six decades; The Africa Adventurers Club and its members; and Afrinc_The Purpose-built African Computer Designed And Used Exclusively By Non-Africans. Tension in the story is sustained by the persistent conflict between Yunhouse and The Africa Adventurers Club, which is set up by Englishmen and Englishwomen who, while being keen on Africa, could not go all the Yunhouse way to the point of “We shall never be pacified”. Founders of The Adventurers insist that Africa is an integral part of the British Empire and therefore deserves “a venue for liberal discussion on the continent that we all love”. In Yunhouse, the reaction is: ‘First we win the right to assert our Africa, and then we’re drawn into the battle of my-Africa-is-more-African-than-yours’.</p>

                        <p>The Adventurers incubates AFC [Africa Freedom Council] a liberation movement to spearhead discussion on self-governance (not necessarily independence) for African colonies. Invitations into the AFC go out from the colonial offices in Africa and draw in Africans who know nothing about Yunhouse. Thus politics in post-colonial Africa is cast into left wing radicalism (Yunism), as opposed to AFC of nondescript ideology – often wearing garb of conservativism.</p>

                        <p>Yunhouse story opens with the Second Battle For Yunhouse at the end of the twentieth century when The Adventurers aimed to take over Yunhouse. It is thought that all Berliner Cult members had died, but unknown to all except Lekwot Abaka before his death, Ogesayi Mawe, an original Berliner turns up in disguise after decades of hiding – following his overthrow from government – during which he achieved international fame as The One And Only Wandering President. Ogesayi Mawe leads Yunhouse to victory.</p>

                        <p>The rest of the story is told by peeling back, event by event, episode by episode, as the characters enter and exit the drama of the debate on Whose Africa Is It Anyway?</p>

                        <p>The final chapter, titled In The Beginning, closes with Big Mnama being on the witness stand in the First Battle for Yunhouse. His niece’s lawyer asks him to defend the legitimacy of running “a den of unpatriotic and irreverent hooligans who spew out treasonable diatribe day and night whilst plotting the destruction of the British Empire”.</p>

                        <p>Readers are challenged to note the human condition implied in colonialism (following shortly after the Trans-Atlantic Holocaust) as a destiny altering transformation of the continent that cradled humanity. African readers in particular are challenged to recognise that colonialism has impacted on what they know and understand about who they really are, today, tomorrow, and for a long time to come.</p>

                        

